function cases = make_v742_weaklink_ablation_cases(spec)
%MAKE_V742_WEAKLINK_ABLATION_CASES Named weak-link hypotheses for v7.4.2.
%
% This intentionally avoids a huge parameter grid. The cases are chosen to
% test interpretable alternatives:
%   broad lane       - the v7.4.1-like control;
%   sparse lane      - fewer weak links along the stressor boundary;
%   hotspots         - compact Josephson bottlenecks;
%   contact edge     - stress relaxation/contact-edge bottlenecks;
%   mixed            - lane plus compact bottlenecks.

if nargin < 1
    spec = struct('name', 'AS006');
end

base = make_v741_weaklink_options(spec);
base.version = 'v7.4.2';

caseRows = { ...
    'v741_reference_broad_lane', 'broad lane control', ...
        0.35, 0.015, 0.012, 1.00, 0.35, 5, 0.65, 1.0, 0.025e-6; ...
    'sparse_lane_moderate', 'sparse boundary lane', ...
        0.10, 0.010, 0.008, 1.25, 0.20, 2, 0.45, 0.6, 0.015e-6; ...
    'sparse_lane_strong', 'sparse boundary lane, lower Ic', ...
        0.06, 0.005, 0.006, 1.35, 0.15, 2, 0.40, 0.6, 0.010e-6; ...
    'ultra_sparse_lane', 'ultra-sparse boundary bottleneck lane', ...
        0.03, 0.003, 0.004, 1.50, 0.10, 1, 0.35, 0.5, 0.006e-6; ...
    'hotspots_moderate', 'compact hotspots, moderate Ic suppression', ...
        0.08, 0.010, 0.008, 0.20, 0.10, 5, 0.45, 2.0, 0.015e-6; ...
    'hotspots_strong', 'compact Josephson-like hotspots', ...
        0.05, 0.004, 0.005, 0.15, 0.05, 6, 0.35, 2.5, 0.008e-6; ...
    'hotspots_ultrasharp', 'compact hotspots, sharpest switching', ...
        0.04, 0.002, 0.003, 0.10, 0.05, 6, 0.30, 2.8, 0.005e-6; ...
    'contact_edge_moderate', 'contact/stressor-edge bottlenecks', ...
        0.10, 0.008, 0.007, 0.25, 1.20, 3, 0.50, 0.8, 0.012e-6; ...
    'contact_edge_strong', 'strong contact/stressor-edge bottlenecks', ...
        0.06, 0.004, 0.005, 0.15, 1.60, 3, 0.40, 0.8, 0.008e-6; ...
    'mixed_sparse', 'sparse lane plus compact hotspots', ...
        0.08, 0.006, 0.006, 0.75, 0.25, 4, 0.40, 1.5, 0.010e-6; ...
    'mixed_ultrasparse', 'ultra-sparse mixed bottlenecks', ...
        0.04, 0.003, 0.004, 0.60, 0.20, 4, 0.32, 1.8, 0.006e-6; ...
    'broad_weak_smooth', 'broad weak lane but smoother switching', ...
        0.20, 0.020, 0.020, 1.00, 0.30, 4, 0.70, 0.8, 0.040e-6 ...
    };

n = size(caseRows, 1);
cases = repmat(struct(), n, 1);

for k = 1:n
    opts = base;
    opts.caseName = caseRows{k, 1};
    opts.description = caseRows{k, 2};
    opts.randomWeakFraction = caseRows{k, 3};
    opts.IcMultiplier = caseRows{k, 4};
    opts.weakSwitchWidthFrac = caseRows{k, 5};
    opts.boundaryWeight = caseRows{k, 6};
    opts.coveredEdgeWeight = caseRows{k, 7};
    opts.numHotspots = caseRows{k, 8};
    opts.hotspotRadius_um = caseRows{k, 9};
    opts.hotspotWeight = caseRows{k, 10};
    opts.currentBroadening_A = caseRows{k, 11};
    opts.seedOffset = base.seedOffset + 100 .* k;
    cases(k).name = string(opts.caseName);
    cases(k).description = string(opts.description);
    cases(k).opts = opts;
end

end
